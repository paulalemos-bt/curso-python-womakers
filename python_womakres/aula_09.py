# operações matematica 

# exemplos de operadores
# + soma
# - subtração
# * multiplicação 
# / divisão
# % Resto
# ** Potenciação

valorA = int(input("Qual é o valor A? "))
valorB = int(input("Qual é o valor B? "))

Resultado_soma = valorA + valorB
Resultado_sub = valorA - valorB

print("#####################################")

print("Soma: ", Resultado_soma)
print("Subtração: ",Resultado_sub)
print("Multiplicação: ", valorA * valorB)
print("Divisão : ", valorA // valorB)
print("Resto da divisão: ", valorA % valorB)
print("Potencia: ", valorA ** valorB)
# Exemplos de operadores 
# += Incremento
# -= Decremento
# % Resto

valorA = int(input("Qual é o valor A? "))
valorB = int(input("Qual é o valor B? "))

valorA += 5
valorB -= 2

print("valor final A: ", valorA)
print("valor final B: ", valorB)

if valorA % 2 == 0:
    print("Numero par") 
else:
    print("Numero impar")    


# operadores relacionais e precedencia 

#exemplos de operadores 
# maior que >
# menor que <
# maior ou igual >=
# menor ou igual <=
# igual a ==
# diferente de !=

valorA = int(input("Qual é o valor A? "))
valorB = int(input("Qual é o valor B? "))

print(valorA > valorB) # saída true
print(valorA < valorB)  # saída false
print(valorA >= valorB) # saída true 
print(valorA <= valorB)  # saída false
print(valorA == valorB) # saída false
print(valorA != valorB) # saída true
